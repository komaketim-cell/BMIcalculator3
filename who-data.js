/**
 * دریافت داده‌های LMS برای سن مشخص (با امکان Interpolation خطی)
 * @param {number} ageInMonths - سن به ماه (60-228)
 * @param {string} gender - 'male' یا 'female'
 * @returns {Object|null} شامل L, M, S یا null در صورت خطا
 */
function getLMS(ageInMonths, gender) {
    const data = gender === 'male' ? WHO_BMI_BOYS : WHO_BMI_GIRLS;

    // بررسی محدوده سن
    if (ageInMonths < 60 || ageInMonths > 228) {
        console.error('سن خارج از محدوده WHO (5-19 سال)');
        return null;
    }

    // اگر سن دقیقاً در داده‌ها وجود دارد
    if (data[ageInMonths]) {
        return data[ageInMonths];
    }

    // Interpolation خطی بین دو نقطه نزدیک
    const lowerAge = Math.floor(ageInMonths);
    const upperAge = Math.ceil(ageInMonths);

    const lowerData = data[lowerAge];
    const upperData = data[upperAge];

    if (!lowerData || !upperData) {
        console.error('خطا در Interpolation');
        return null;
    }

    // محاسبه وزن Interpolation
    const weight = ageInMonths - lowerAge;

    const L = lowerData.L + (upperData.L - lowerData.L) * weight;
    const M = lowerData.M + (upperData.M - lowerData.M) * weight;
    const S = lowerData.S + (upperData.S - lowerData.S) * weight;

    return { L, M, S };
}
